/**
 * @file hello_world.c
 * @brief Simple program to print "Hello World".
 *
 * This program demonstrates the basic structure of a C program.
 * It uses the printf() function to display a message on the console.
 */

#include <stdio.h>

/**
 * @brief Entry point of the program.
 *
 * The program prints "Hello World" followed by a newline character.
 *
 * @return int Returns 0 on successful execution.
 */
int main(void)
{
    /* Print greeting message */
    printf("Hello World\n");

    return 0;
}